package kishan;

public class Staff extends AuditFields {
	private String name;
	private int id;
	Staff() {

	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

}
